def F(n):
    """Нахождение всех делителей числа,
    не считая единицы и самого числа."""
    arr = []
    d = 2
    while d * d <= n:
        if n % d == 0:
            arr.extend([d, n // d])
        d += 1
    return sorted(arr)

    
for n in range(174457, 174505+1):
    if len(F(n)) == 2:
        print(*F(n))


